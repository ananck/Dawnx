namespace Dawnx.Definition.Unit
{
    public static class Storage
    {
        /// <summary>
        /// Unit: Byte
        /// </summary>
        public const long B = 1;

        /// <summary>
        /// Unit: Byte
        /// </summary>
        public const long KB = 1024 * B;

        /// <summary>
        /// Unit: Byte
        /// </summary>
        public const long MB = 1024 * KB;

        /// <summary>
        /// Unit: Byte
        /// </summary>
        public const long GB = 1024 * MB;

        /// <summary>
        /// Unit: Byte
        /// </summary>
        public const long TB = 1024 * GB;

        /// <summary>
        /// Unit: Byte
        /// </summary>
        public const long PB = 1024 * TB;

        /// <summary>
        /// Unit: Byte
        /// </summary>
        public const long EB = 1024 * PB;
    }
}
